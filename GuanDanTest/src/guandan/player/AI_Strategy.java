package guandan.player;

import java.util.ArrayList;
import guandan.game.Poker;

;
public class AI_Strategy {
	private double hands;
	private int[] numList;
	private int[][] pokerList0;
	private ArrayList<Poker[]> straightFlushList;
	private ArrayList<Poker[]> statightList;
	private ArrayList<Poker[]> tripleDouble;
	private ArrayList<Poker[]> doubleTriple;

	public AI_Strategy(double hands, int[] numList, int[][] pokerList0,
			ArrayList<Poker[]> sF, ArrayList<Poker[]> s, ArrayList<Poker[]> tD,
			ArrayList<Poker[]> dT) {
		this.hands = hands;
		this.numList = numList;
		this.pokerList0 = pokerList0;
		this.straightFlushList = sF;
		this.statightList = s;
		this.tripleDouble = tD;
		this.doubleTriple = dT;
	}

	public double getHands() {
		return this.hands;
	}

	public int[] getNumList() {
		return this.numList;
	}

	public int[][] getPokerList0() {
		return this.pokerList0;
	}

	public ArrayList<Poker[]> getStraightFlush() {
		return this.straightFlushList;
	}

	public ArrayList<Poker[]> getStraight() {
		return this.statightList;
	}

	public ArrayList<Poker[]> getTripleDouble() {
		return this.tripleDouble;
	}

	public ArrayList<Poker[]> getDoubleTriple() {
		return this.doubleTriple;
	}

	public void setHands(double hands) {
		this.hands = hands;
	}

	public void setNumList(int[] numList) {
		this.numList = numList;
	}

	public void setPokerList0(int[][] pokerList0) {
		this.pokerList0 = pokerList0;
	}

	public void setStraightFlush(ArrayList<Poker[]> sF) {
		this.straightFlushList = sF;
	}

	public void setStraight(ArrayList<Poker[]> s) {
		this.statightList = s;
	}

	public void setTripleDouble(ArrayList<Poker[]> tD) {
		this.tripleDouble = tD;
	}

	public void setDoubleTriple(ArrayList<Poker[]> dT) {
		this.doubleTriple = dT;
	}
}
